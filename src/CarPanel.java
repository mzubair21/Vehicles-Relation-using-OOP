    private void mpg1BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mpg1BtnActionPerformed
       int year = 0,doors =0,fuel =0,drivingRange =0,load = 0;
       try{
         year = Integer.parseInt(txt3.getText());
       }catch(NumberFormatException e){
          JOptionPane.showMessageDialog (null, "Year Should be in Number \n", "Invalid Information", JOptionPane.ERROR_MESSAGE);  
       }
       try{
         doors = Integer.parseInt(txt4.getText());
       }catch(NumberFormatException e){
          JOptionPane.showMessageDialog (null, "Doors Should be in Number \n", "Invalid Information", JOptionPane.ERROR_MESSAGE);  
       }
        try{
         fuel = Integer.parseInt(txt5.getText());
       }catch(NumberFormatException e){
          JOptionPane.showMessageDialog (null, "Fuel Tank Capacity Should be in Number \n", "Invalid Information", JOptionPane.ERROR_MESSAGE);  
       }
        try{
         drivingRange = Integer.parseInt(txt6.getText());
       }catch(NumberFormatException e){
          JOptionPane.showMessageDialog (null, "Driving Range Should be in Number \n", "Invalid Information", JOptionPane.ERROR_MESSAGE);  
       }
        try{
         load = Integer.parseInt(txt7.getText());
       }catch(NumberFormatException e){
          JOptionPane.showMessageDialog (null, "Extra Load Should be in Number \n", "Invalid Information", JOptionPane.ERROR_MESSAGE);  
       }
        Vehicle car = new Vehicle(txt1.getText(),txt2.getText(),year,doors,fuel,drivingRange);
        txtMPG.setText(car.calcMPG()+""); 
        
    }//GEN-LAST:event_mpg1BtnActionPerformed

